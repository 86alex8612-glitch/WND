def get_stage_from_message(user_message: str, history: list = None) -> str:
    """Определить этап анализа на основе сообщения пользователя и истории"""
    message_lower = user_message.lower()
    
    # Проверяем ключевые слова
    for stage, keywords in STAGE_KEYWORDS.items():
        for keyword in keywords:
            if keyword in message_lower:
                return stage
    
    # Если история пуста — начинаем с идентификации
    if not history or len(history) == 0:
        return "identification"
    
    # Определяем этап по количеству сообщений в истории
    msg_count = len(history)
    if msg_count <= 2:
        return "analysis"
    elif msg_count <= 4:
        return "comparison"
    elif msg_count <= 6:
        return "recommendations"
    elif msg_count <= 8:
        return "report"
    else:
        return "finalization"

def get_agent_prompt(stage: str) -> str:
    """Получить промпт для агента по этапу"""
    return STAGE_AGENTS.get(stage, AGENT_ANALYST)
